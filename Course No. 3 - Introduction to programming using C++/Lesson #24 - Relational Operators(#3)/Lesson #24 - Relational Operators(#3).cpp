#include <iostream>
using namespace std;

int main()
{
	short a, b;
	cout << "Enter number A \n";
	cin >> a;

	cout << "Enter number B\n";
	cin >> b;

	cout << " A == B is : " << (a == b)<<endl;
	cout << " A != B is : " << (a != b) << endl;
	cout << " A <  B is : " << (a < b) << endl;
	cout << " A >  B is : " << (a > b) << endl;
	cout << " A >= B is : " << (a >= b) << endl;
	cout << " A <= B is : " << (a <= b) << endl;
	

}
